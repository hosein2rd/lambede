package com.lambede.lamer.utilities;

public class HistoryItems {

    public int image;
    public String title;
    public String date;
    public String cost;
    public String details;
    public String addrress;

    public HistoryItems(int image, String title, String date, String cost, String details, String addrress){
        this.image = image;
        this.title = title;
        this.date = date;
        this.cost = cost;
        this.details = details;
        this.addrress = addrress;
    }

}
